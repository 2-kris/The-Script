#!/bin/bash

#This script is to welcome user with system info
#Script by Krishna Misal


echo -----------------"Welcome, $(whoami)"-------------------------------------------

echo -n "Today is " && date | awk '{print $1,$3,$2,$6,$5,$4}'

echo --------------------------------------------------------------------------------

echo "system uptime"
uptime -p

echo --------------------------------------------------------------------------------
echo -n "Available Disk Space:  "

df -h | xargs | awk '{print $11,"of",$9}'


echo --------------------------------------------------------------------------------
echo -n "Available Memory: "

free -h | xargs | awk '{print $13,"of",$8}'

echo --------------------------------------------------------------------------------

echo "Last 5 logs"
last | head -5


echo --------------------------------------------------------------------------------

echo -------------------------------"LETS ROLL"--------------------------------------

echo --------------------------------------------------------------------------------
